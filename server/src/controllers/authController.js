const User = require("../models/UserModel");
const { createSecretToken } = require("../util/SecretToken");
const bcrypt = require("bcryptjs");

module.exports.Signup = async (req, res, next) => {
  try {
    const { email, password, username, createdAt } = req.body;
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.json({ message: "User already exists" });
    }
    const user = await User.create({ email, password, username, createdAt });
    const token = createSecretToken(user._id);
    res.cookie("token", token, {
      withCredentials: true,
      httpOnly: false,
    });
    res
      .status(201)
      .json({ message: "User signed in successfully", success: true, user });
    next();
  } catch (error) {
    console.error(error);
  }
};

module.exports.Login = async (req, res, next) => {
  try {
    const { email, password } = req.body;
    if(!email || !password ){
      return res.json({message:'All fields are required'})
    }
    const user = await User.findOne({ email });
    if(!user){
      return res.json({message:'Incorrect password or email' }) 
    }
    const auth = await bcrypt.compare(password,user.password)
    if (!auth) {
      return res.json({message:'Incorrect password or email' }) 
    }
     const token = createSecretToken(user._id);
     res.cookie("token", token, {
       withCredentials: true,
       httpOnly: false,
     });
     res.status(201).json({ message: "User logged in successfully", success: true });
     next()
  } catch (error) {
    console.error(error);
  }
};

module.exports.Logout = (req, res) => {
  try {
    // Clear the token cookie
    res.clearCookie('token', {
      withCredentials: true,
      httpOnly: false,
    });

    // Optionally, you can send a success message or status to the client
    // The client side should handle the actual redirection based on this response
    res.json({ message: "User logged out successfully"});

    // If you're strictly following REST principles, you might not send a redirect command from the server.
    // Instead, the front-end would decide to navigate to the signup page upon receiving the successful logout response.
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: "An error occurred during the logout process", redirect: "/signup" });
  }
};
